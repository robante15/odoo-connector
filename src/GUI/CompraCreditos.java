    private void btn_aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_aceptarActionPerformed
        String codigoIngresado = String.valueOf(this.psw_codSeguridad.getPassword());
        if (codigoIngresado.equals("")) {
            JOptionPane.showMessageDialog(null, "No deje los campos vacios");
        } else {
            factory = new Factory();
            BaseDatos BD = factory.baseDatos();
            historial_creditos_extraBD creditosBD = factory.historial_creditos_extraBD();
            EmpresaBD empresaBD = factory.empresaBD();

            String HashedCode = listaTarjetas.get(this.cbox_metodoPago.getSelectedIndex()).getCodigo_seguridad();

            if (BD.checkPassword(codigoIngresado, HashedCode) == true) {
                int cantidad_creditos = (Integer) this.spn_cantidadCreditos.getValue();
                double total = cantidad_creditos;
                int metodo_pago_id = listaTarjetas.get(this.cbox_metodoPago.getSelectedIndex()).getId();
                historial_creditos_extra HistoCred = factory.historial_creditos_extra('0', empresa.getId(), " ", cantidad_creditos, total, metodo_pago_id);

                boolean flag1 = creditosBD.guardarCreditosExtra(HistoCred);
                boolean flag2 = empresaBD.agregarCreditosExtra(empresa.getId(), cantidad_creditos);

                if (flag1 == true && flag2 == true) {
                    JOptionPane.showMessageDialog(null, "Nuevo registro agregado correctamente");
                } else {
                    JOptionPane.showMessageDialog(null, "Ha ocurrido un error al actualizar el registro");
                }

            } else {
                JOptionPane.showMessageDialog(null, "El codigo de seguridad no es correcto");
            }

        }
    }//GEN-LAST:event_btn_aceptarActionPerformed


    private void btn_agregarMPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_agregarMPActionPerformed

        if (txt_propietario.getText().equals("") || txt_numTarjeta.getText().equals("")
                || txt_fechaVencimiento.getText().equals("") || String.valueOf(this.psw_codigoSeguridad.getPassword()).equals("") || txtA_DireccionTarjeta.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "No deje los campos vacios");
        } else {
            factory = new Factory();
            BaseDatos BD = factory.baseDatos();
            informacion_bancariaBD infBankBD = factory.informacion_bancariaBD();

            informacion_bancaria newInfoBank;

            String hashedCodSeg = BD.hashPassword(String.valueOf(this.psw_codigoSeguridad.getPassword()));

            String propietario = this.txt_propietario.getText();
            String numero_tarjeta = this.txt_numTarjeta.getText();
            String tipo_tarjeta = this.cbox_tipoTarjeta.getItemAt(this.cbox_tipoTarjeta.getSelectedIndex());
            String fecha_vencimiento = this.txt_fechaVencimiento.getText();
            String codigo_seguridad = hashedCodSeg;
            String direccion_tarjeta = this.txtA_DireccionTarjeta.getText();
            int empresa_id = empresa.getId();

            newInfoBank = factory.informacion_bancaria(0, propietario, numero_tarjeta, tipo_tarjeta, fecha_vencimiento, codigo_seguridad, direccion_tarjeta, empresa_id);

            infBankBD.insertarInformacionBancaria(newInfoBank);
        }
    }//GEN-LAST:event_btn_agregarMPActionPerformed

